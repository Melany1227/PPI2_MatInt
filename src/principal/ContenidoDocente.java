    private void actualizarArchivoYTabla(boolean actualizarEnArchivo, boolean actualizarEnTabla) {
    DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

    try {
        FileWriter fw = new FileWriter("Tareas.txt");
        BufferedWriter bw = new BufferedWriter(fw);

        // Recorre las filas de la tabla y guarda los datos en el archivo
        for (int i = 0; i < modelo.getRowCount(); i++) {
            for (int j = 0; j < modelo.getColumnCount(); j++) {
                if (j == 1) {
                    // Si es la columna de tema, verifica si la fila está siendo editada
                    if (i == filaSeleccionada && actualizarEnArchivo) {
                        // Si es la fila en edición, escribe el nuevo valor del campo de tema
                        bw.write(txtTema.getText());
                    } else {
                        // Si no es la fila en edición, escribe el valor actual de la celda
                        bw.write(modelo.getValueAt(i, j).toString());
                    }
                } else {
                    bw.write(modelo.getValueAt(i, j).toString());
                }
                bw.write(";");
            }
            bw.newLine();
        }

        bw.close();
        fw.close();

        if (actualizarEnTabla) {
            // Si se debe actualizar en la tabla, vuelve a cargar los datos desde el archivo
            DefaultTableModel mdlTabla = actualizarDatos();
            jTable1.setModel(mdlTabla);
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
}


    
    public boolean existeCurso(String courseCode) {
        try {
            BufferedReader br = new BufferedReader(new FileReader("curso.txt"));
            String line;

            while ((line = br.readLine()) != null) {
                String[] datos = line.split(";");
                if (datos.length >= 3) {
                    String code = datos[0];

                        if (courseCode.equals(code)) {
                        br.close();
                        return true; 
                    }
                }
            }

            br.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    return false;  
    }
    
    private void abrirDocumentoDesdeTabla(String rutaCompleta) {
        try {
            abrirArchivo(rutaCompleta);
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al abrir el documento: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void abrirArchivo(String rutaCompleta) throws IOException {
        File archivo = new File(rutaCompleta);

        if (archivo.exists()) {
            Desktop.getDesktop().open(archivo);
        } else {
            JOptionPane.showMessageDialog(this, "El archivo no existe: " + rutaCompleta, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void guardarInformacionEnArchivo(String codigo, String tema, String nombreArchivo) {
        try (BufferedWriter writer = new BufferedWriter(new FileWriter("Tareas.txt", true))) {
            File archivoSeleccionado = new File(nombreArchivo);
            String rutaCompleta = archivoSeleccionado.getAbsolutePath();
            writer.write(codigo + ";" + tema + ";" + rutaCompleta);
            writer.newLine();
        } catch (IOException e) {
            System.err.println("Error al escribir en el archivo: " + e.getMessage());
            e.printStackTrace();
        }
    }
    
    //Método para actualizar los datos en la tabla
    public DefaultTableModel actualizarDatos() throws FileNotFoundException {
        Vector cabeceras = new Vector();
        cabeceras.addElement("Código");
        cabeceras.addElement("Tema");
        cabeceras.addElement("Doc");

        DefaultTableModel mdlTabla = new DefaultTableModel(cabeceras, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        try {
            FileReader fr = new FileReader("Tareas.txt");
            BufferedReader br = new BufferedReader(fr);
            String d;
            while ((d = br.readLine()) != null) {
                StringTokenizer dato = new StringTokenizer(d, ";");
                Vector x = new Vector();
                while (dato.hasMoreTokens()) {
                    x.addElement(dato.nextToken());
                }
                mdlTabla.addRow(x);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        return mdlTabla;
    }

