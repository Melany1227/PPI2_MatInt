    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int seleccion;

        String codigo = txtCodC.getText().trim();
        String tema = txtTema.getText().trim();

        // Validar que el código del curso tenga exactamente 4 dígitos numéricos
        if (codigo.length() != 4 || !codigo.matches("\\d{4}")) {
            JOptionPane.showMessageDialog(this, "El código del curso debe tener exactamente 4 dígitos numéricos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Verificar si el curso existe
        if (!existeCurso(codigo)) {
            JOptionPane.showMessageDialog(this, "El código del curso no existe.", "Error", JOptionPane.ERROR_MESSAGE);
            txtCodC.setText("");
            return;
        }

        // Validar que se haya ingresado texto en el campo de tema y que solo sean letras
        if (tema.isEmpty() || !tema.matches("^[a-zA-Z]+$")) {
            JOptionPane.showMessageDialog(this, "Por favor, ingrese un tema válido con solo letras.", "Error", JOptionPane.ERROR_MESSAGE);
            txtTema.setText("");
            return;
        }

        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos PDF y Word", "pdf", "doc", "docx");
        fileChooser.setFileFilter(filter);
        seleccion = fileChooser.showOpenDialog(this);

        jTable1.getSelectionModel().addListSelectionListener(new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                int filaSeleccionada = jTable1.getSelectedRow();
                if (filaSeleccionada != -1) {
                    String codigo = jTable1.getValueAt(filaSeleccionada, 0).toString();
                    String tema = jTable1.getValueAt(filaSeleccionada, 1).toString();

                    txtCodC.setText(codigo);
                    txtTema.setText(tema);
                }
            }
        });

        guardarInformacionEnArchivo(codigo, tema, fileChooser.getSelectedFile().getAbsolutePath());
        try {
            DefaultTableModel mdlTabla = actualizarDatos(); 
            jTable1.setModel(mdlTabla); 
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ContenidoDocente.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "Contenido agregado correctamente.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton1ActionPerformed



