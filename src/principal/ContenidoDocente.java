    public void actualizarTxt(String codigo, String tema, String tarea) {
        try {
            File inFile = new File("TareasTemp.txt");
            File outFile = new File("Tareas.txt");

            BufferedReader br = new BufferedReader(new FileReader(inFile));
            BufferedWriter bw = new BufferedWriter(new FileWriter(outFile));

            String line;
            while ((line = br.readLine()) != null) {
                String[] datos = line.split(";");
                if (datos.length >= 4) {
                    String codigoTarea = datos[0];
                    if (!codigoTarea.equals(codigo) || !datos[1].equals(tema) || !datos[2].equals(tarea)) {
                        bw.write(line);
                        bw.newLine();
                    }
                }
            }

            br.close();
            bw.close();
        } catch (IOException e) {
            e.printStackTrace();
            System.err.println("Hubo un error al actualizar el archivo.");
        }
    }



    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        txtCodC.setEditable(false);

        // Verifica si se seleccionó una fila para editar
        if (filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una fila para editar.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Obtén los valores actuales de la fila seleccionada
        String codigo = modelo.getValueAt(filaSeleccionada, 0).toString();
        String tema = modelo.getValueAt(filaSeleccionada, 1).toString();
        String tarea = modelo.getValueAt(filaSeleccionada, 2).toString();

        // Verifica si el contenido del campo de tema ha cambiado
        if (!txtTema.getText().equals(tema)) {
            // Si ha cambiado, actualiza el valor en el modelo de la tabla
            modelo.setValueAt(txtTema.getText(), filaSeleccionada, 1);

            // Actualiza el archivo "Tareas.txt" con los nuevos datos y la tabla
            actualizarArchivoYTabla(true, false);

            JOptionPane.showMessageDialog(this, "Contenido editado correctamente.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Si no ha cambiado, muestra un mensaje informativo
            JOptionPane.showMessageDialog(this, "No se realizaron cambios.", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        }



    }//GEN-LAST:event_btnEditarActionPerformed

